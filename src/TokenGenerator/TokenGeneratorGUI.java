    private void UpdateLogs(String PatientType, String TokenNum){
        String Date_Time = GetSystemTime();
        try {
            File f1 = new File("TokenLogs.txt");
            if(!f1.exists()) {
            f1.createNewFile();
        }
            FileWriter fileWritter = new FileWriter(f1.getName(),true);
            BufferedWriter bw = new BufferedWriter(fileWritter);
            bw.write("Patient Name: " + jTextField1.getText() + "\n");
            bw.write("Patient Type: " + PatientType + "\n");
            bw.write("Token Number: " + TokenNum + "\n");
            bw.write("Date/Time: " + Date_Time + "\n");
            bw.write("**************************************************\n");
            bw.close();
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, "Error Updating Logs.");
        }
    }
