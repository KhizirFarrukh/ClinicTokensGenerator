    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String lineString = jTextField1.getText();
        if(lineString.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Enter TEXT First!");
        }
        else
        {
            int AntiCharFlag=0;
            for (int i = 0; i < lineString.length(); i++) {
                if(!(lineString.charAt(i)==' ' || (lineString.charAt(i)>='A' && lineString.charAt(i)<='Z') || (lineString.charAt(i)>='a' && lineString.charAt(i)<='z')))
                {
                    AntiCharFlag=1;
                }
            }
            if(AntiCharFlag==1)
            {
                JOptionPane.showMessageDialog(null, "Name cannot contain Numbers or Symbols!");
                jTextField1.setText("");
            }
            else
            {
                int num = Integer.parseInt(jTextPane1.getText());
                num++;
                jTextPane1.setText(String.valueOf(num));
                PrintToken("MALE", jTextPane1.getText());
                UpdateLogs("MALE", jTextPane1.getText());
                jTextField1.setText("");
                String OldString, NewString;
                OldString=jTextArea1.getText();
                if(OldString.equals(""))
                {
                    NewString=String.valueOf(num)+" - "+lineString+"\n";
                }
                else
                {
                    NewString=OldString+String.valueOf(num)+" - "+lineString+"\n";
                }
                WriteToFile("MData.bin", String.valueOf(num), NewString);
                jTextArea1.setText(NewString);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

